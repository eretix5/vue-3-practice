<template>
    <input :value="modelValue" @input="updateInput" class="input" type="text">
</template>

<script>
    export default {
        name: "my-input",
        props: {
            modelValue: [String, Number]
        },
        methods: {
            updateInput(event){
                this.$emit('update:modelValue', event.target.value)
            }
        }
    }
</script>

<style scoped>
    .input{
        border: 1px solid #001D6E;
        margin-top: 15px;
        padding: 10px 15px;
        width: 100%;
    }
</style>